public class Book {
	// Book类的的属性name,pages
	private String name;
	private int pages;

	// 无参构造方法
	public Book() {

	}

	// 有参构造方法
	public Book(String name, int pages) {
		this.name = name;
		this.pages = pages;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getPages() {
		return pages;
	}

	public void setPages(int pages) {
		this.pages = pages;
	}

	// get和set方法




}
